function getColor(l) {
    return l > 99.5  ? '#B3FFA0' :
           l > 99    ? '#C6FFA0' :
           l > 98.5  ? '#D9FFA0' :
           l > 98    ? '#ECFFA0' :
           l > 97.5  ? '#FFFFA0' :
           l > 97    ? '#FFECA0' :
           l > 96.5  ? '#FFD9A0' :
		   l > 96    ? '#FFC6A0' :
		   l > 95.5  ? '#FFB3A0' :
		   l > 95    ? '#FFA0A0' :
		   l > 94.5  ? '#FFA0B3' :
		               '#FFE9ED' ;
}
function style(feature) {
    return {
        fillColor: getColor(feature.properties.literacy),
        weight: 2,
        opacity: 1,
        color: 'white',
        dashArray: '3',
        fillOpacity: 0.7
    };
}
function highlightFeature(e) {
    var layer = e.target;

    layer.setStyle({
        weight: 5,
        color: '#666',
        dashArray: '',
        fillOpacity: 0.7
    });

    if (!L.Browser.ie && !L.Browser.opera) {
        layer.bringToFront();
    }
}
function resetHighlight(e) {
    geojson.resetStyle(e.target);
}
function zoomToFeature(e) {
    map.fitBounds(e.target.getBounds());
}
function onEachFeature(feature, layer) {
    layer.on({
        mouseover: highlightFeature,
        mouseout: resetHighlight,
        click: zoomToFeature
    });
}
function getColour(d) {
    switch (d) {
        case '99.5-99.9%': return '#B3FFA0';
        case '99-99.5%': return '#C6FFA0';
        case '98.5-99%': return '#D9FFA0';
        case '98-98.5%': return '#ECFFA0';
        case '97.5-98%': return '#FFFFA0';
        case '97-97.5%': return '#FFECA0';
        case '96.5-97%': return '#FFD9A0';
        case '96-96.5%': return '#FFC6A0';
        case '95.5-96%': return '#FFB3A0';
        case '95-95.5%': return '#FFA0A0';
        case '94.5-95%': return '#FFA0B3';
        default: return '#FFE9ED';
    }
};

